// ========== 绘制函数 ==========

export interface SceneObjectInstance {
  id: string;
  objectType: ObjectType;
  x: number;
  y: number;
  scale?: number;
  interactive?: boolean;
}

export class SceneObjectRenderer {
  private time: number = 0;
  private frameIndices: Map<string, number> = new Map();
  
  update(dt: number) {
    this.time += dt * 1000;
  }
  
  drawObject(
    ctx: CanvasRenderingContext2D,
    instance: SceneObjectInstance,
    ambientColor: string = '#ffffff'
  ) {
    const obj = SCENE_OBJECTS[instance.objectType];
    if (!obj) {
      console.warn(`Unknown object type: ${instance.objectType}`);
      return;
    }
    
    const scale = instance.scale || 2;
    const frameIndex = this.getFrameIndex(instance.id, obj);
    const frame = obj.frames[frameIndex];
    
    // 绘制阴影
    if (obj.hasShadow) {
      this.drawShadow(ctx, instance.x, instance.y, obj.width * scale, obj.height * scale);
    }
    
    // 绘制像素物体
    this.drawPixelFrame(ctx, frame, instance.x, instance.y, scale, ambientColor);
  }
  
  private getFrameIndex(id: string, obj: PixelObject): number {
    if (!obj.animationSpeed || obj.frames.length <= 1) return 0;
    
    // 动画帧切换
    const frameCount = obj.frames.length;
    const frameTime = obj.animationSpeed;
    const index = Math.floor(this.time / frameTime) % frameCount;
    return index;
  }
  
  private drawShadow(ctx: CanvasRenderingContext2D, x: number, y: number, w: number, h: number) {
    ctx.fillStyle = 'rgba(0,0,0,0.25)';
    ctx.beginPath();
    ctx.ellipse(x + w / 2, y + h + 5, w * 0.4, h * 0.08, 0, 0, Math.PI * 2);
    ctx.fill();
  }
  
  private drawPixelFrame(
    ctx: CanvasRenderingContext2D,
    frame: number[][],
    x: number,
    y: number,
    scale: number,
    ambientColor: string
  ) {
    const halfW = (frame[0].length * scale) / 2;
    const halfH = (frame.length * scale) / 2;
    
    frame.forEach((row, py) => {
      row.forEach((colorIndex, px) => {
        if (colorIndex === 0) return;
        
        const color = this.getColor(colorIndex, ambientColor);
        ctx.fillStyle = color;
        ctx.fillRect(
          x + px * scale - halfW,
          y + py * scale - halfH,
          scale,
          scale
        );
      });
    });
  }
  
  private getColor(index: number, ambientColor: string): string {
    const baseColor = COLOR[index] || '#ffffff';
    
    // 应用环境光色调
    if (index === 1 || index === 8) {
      // 白色和金色受环境光影响
      return this.blendColors(baseColor, ambientColor, 0.2);
    }
    
    return baseColor;
  }
  
  private blendColors(color1: string, color2: string, ratio: number): string {
    // 简单的颜色混合
    const r1 = parseInt(color1.slice(1, 3), 16);
    const g1 = parseInt(color1.slice(3, 5), 16);
    const b1 = parseInt(color1.slice(5, 7), 16);
    
    const r2 = parseInt(color2.slice(1, 3), 16);
    const g2 = parseInt(color2.slice(3, 5), 16);
    const b2 = parseInt(color2.slice(5, 7), 16);
    
    const r = Math.round(r1 * (1 - ratio) + r2 * ratio);
    const g = Math.round(g1 * (1 - ratio) + g2 * ratio);
    const b = Math.round(b1 * (1 - ratio) + b2 * ratio);
    
    return `#${r.toString(16).padStart(2, '0')}${g.toString(16).padStart(2, '0')}${b.toString(16).padStart(2, '0')}`;
  }
  
  // 检测碰撞
  checkCollision(
    charX: number,
    charY: number,
    charRadius: number,
    instance: SceneObjectInstance
  ): boolean {
    const obj = SCENE_OBJECTS[instance.objectType];
    if (!obj || !obj.collisionBox) return false;
    
    const scale = instance.scale || 2;
    const box = obj.collisionBox;
    
    const objLeft = instance.x - (box.width * scale) / 2;
    const objRight = instance.x + (box.width * scale) / 2;
    const objTop = instance.y + (box.offsetY * scale);
    const objBottom = objTop + box.height * scale;
    
    // 简单的圆形-矩形碰撞检测
    const closestX = Math.max(objLeft, Math.min(charX, objRight));
    const closestY = Math.max(objTop, Math.min(charY, objBottom));
    
    const distance = Math.sqrt(
      Math.pow(charX - closestX, 2) + Math.pow(charY - closestY, 2)
    );
    
    return distance < charRadius;
  }
}

